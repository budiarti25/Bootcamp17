    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        boolean flag = this.jobController.save(txtId.getText(), txtTitle.getText(), txtMin.getText(), txtMax.getText());
        String message = "Failed to save data...";
        if (flag) {
            message = "Success to save data...";
        }
        JOptionPane.showMessageDialog(this, message, "Notification", JOptionPane.INFORMATION_MESSAGE);
        bindingTable();
        reset();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDropActionPerformed
        String message = "Failed to delete data";
        int flag = JOptionPane.showConfirmDialog(this, "Delete this row?", "Delete Confirmation?", JOptionPane.YES_NO_OPTION);
        if (flag == 0) {
            if (jobController.drop(txtId.getText())) {
                message = "Success to delete data";
            }
            JOptionPane.showMessageDialog(this, message, "Notification", JOptionPane.WARNING_MESSAGE);
        }
        bindingTable();
        reset();
    }//GEN-LAST:event_btnDropActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String category = cmbCategory.getSelectedItem().toString();
        String data = txtSearch.getText();
        searchTable(category, data);
    }//GEN-LAST:event_btnSearchActionPerformed

