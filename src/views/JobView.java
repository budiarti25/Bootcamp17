    public void bindingTable() {
        String[] header = {"Job ID", "Job Title", "Min Salary", "Max Salary"};
        DefaultTableModel defaultTableModel = new DefaultTableModel(header, 0);
        for (Job job : jobController.binding()) {
            Object[] job1 = {
                job.getJobId(), job.getJobTitle(), job.getMinSalary(), job.getMaxSalary()
            };
            defaultTableModel.addRow(job1);
        }
        tblJob.setModel(defaultTableModel);
    }

    public void reset() {
        txtId.setText("");
        txtTitle.setText("");
        txtMin.setText("");
        txtMax.setText("");
    }

    public void searchTable(String category, String data) {
        String[] header = {"Job ID", "Job Title", "Min Salary", "Max Salary"};
        DefaultTableModel defaultTableModel = new DefaultTableModel(header, 0);
        for (Job job : jobController.find(category, data)) {
            Object[] job1 = {
                job.getJobId(), job.getJobTitle(), job.getMinSalary(), job.getMaxSalary()
            };
            defaultTableModel.addRow(job1);
        }
        tblJob.setModel(defaultTableModel);
    }

